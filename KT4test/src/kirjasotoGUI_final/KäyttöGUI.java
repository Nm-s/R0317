    private void Btn_LisääActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_LisääActionPerformed
        
        if(tarkistusInput() && ImgPath != null)
        {
            try {
                 Connection con = getConnection();
                PreparedStatement ps = con.prepareStatement("INSERT INTO products(teoksennimi,tekijänimi,julkaisuvuosi,image)"
                        + "values(?,?,?,?) ");
                ps.setString(1, txt_tekijänimi.getText());
                ps.setString(2, txt_teoksennimi.getText());
                ps.setString(3, txt_julkaisuvuosi.getText());
               
                
                InputStream img = new FileInputStream(new File(ImgPath));
                ps.setBlob(4, img);
                Tiedot_Jtableen();
                
                JOptionPane.showMessageDialog(null, "Tiedot Lisätty");
            } catch (Exception ex) {
                 JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        }else{
            JOptionPane.showMessageDialog(null, "Virhe");
        }
        
        System.out.println("Tekijännimi => "+txt_tekijänimi.getText());
        System.out.println("Teoksennimi => "+txt_teoksennimi.getText());
        System.out.println("Julkaisuvuosi => "+txt_julkaisuvuosi.getText());
        System.out.println("Image => "+ImgPath);
    }//GEN-LAST:event_Btn_LisääActionPerformed

    
