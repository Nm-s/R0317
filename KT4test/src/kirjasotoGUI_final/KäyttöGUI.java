    // Tiedot JTableen
    
    public ArrayList<Tiedot> getTiedotLista()
    {
            ArrayList<Tiedot> tiedotLista  = new ArrayList<Tiedot>();
            Connection con = getConnection();
            String query = "SELECT * FROM products";
            
            Statement st;
            ResultSet rs;
            
        try {
            
            st = con.createStatement();
            rs = st.executeQuery(query);
            Tiedot tiedot;
            
            while(rs.next())
            {
                tiedot = new Tiedot(rs.getInt("julkaisuvuosi"),rs.getString("tekijänimi"),rs.getString("teoksennimi"),rs.getBytes("image"));
                tiedotLista.add(tiedot);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(KäyttöGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        return tiedotLista; 
                
    }
    
    
