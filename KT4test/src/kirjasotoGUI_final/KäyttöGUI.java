    public void Tiedot_Jtableen()
    {
        ArrayList<Tiedot> list = getTiedotLista();
        DefaultTableModel model = (DefaultTableModel)JTable_Tiedot.getModel();
        model.setRowCount(0);
        Object[] row = new Object[3];
        for(int i = 0; i < list.size(); i++)
        {
            row[0] = list.get(i).getJulkaisuvuosi();
            row[1] = list.get(i).getTekijänimi();
            row[2] = list.get(i).getTeoksennimi();
          
            
            model.addRow(row);
        }
    
    }
    
